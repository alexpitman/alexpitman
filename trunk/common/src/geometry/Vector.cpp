#include "geometry/Vector.h"
///////////////////////////////////////////////////////////////////////////////
//
// Name           : Vector2D
// Inheritance    : Base struct 
// Desctription   : See header.
//
///////////////////////////////////////////////////////////////////////////////

#include "numeric/num.h"

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D::Vector2D() {}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D::Vector2D(const double X, const double Y)
{
  myX = X;
  myY = Y;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D&
geo::Vector2D::operator =(const Vector2D& RHS)
{
  myX = RHS.X();
  myY = RHS.Y();
  return *this;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D
geo::Vector2D::operator +(const Vector2D& RHS) const
{
  return Vector2D( myX + RHS.X(), myY + RHS.Y() );
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D
geo::Vector2D::operator -(const Vector2D& RHS) const
{
  return Vector2D( myX - RHS.X(), myY - RHS.Y() );
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D& 
geo::Vector2D::operator +=(const Vector2D& RHS)
{
  myX += RHS.X();
  myY += RHS.Y();
  return *this; 
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D& 
geo::Vector2D::operator -=(const Vector2D& RHS)
{
  myX -= RHS.X();
  myY -= RHS.Y();
  return *this;
}

///////////////////////////////////////////////////////////////////////////////
bool 
geo::Vector2D::operator ==(const Vector2D& RHS) const
{
  return ( myX == RHS.X() && myY == RHS.Y() );
}

///////////////////////////////////////////////////////////////////////////////
double 
geo::Vector2D::Length() const
{
  return sqrt( LengthSquared() );
}

///////////////////////////////////////////////////////////////////////////////
double 
geo::Vector2D::LengthSquared() const
{
  return myX * myX + myY * myY;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D& 
geo::Vector2D::Zero()
{
  static Vector2D vec(0, 0);
  return vec;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D& 
geo::Vector2D::UnitX()
{
  static Vector2D vec(1, 0);
  return vec;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector2D& 
geo::Vector2D::UnitY()
{
  static Vector2D vec(0, 1);
  return vec;
}

///////////////////////////////////////////////////////////////////////////////
bool 
geo::Vector2D::operator !=(const Vector2D& RHS) const
{
  return !operator==(RHS);
}

///////////////////////////////////////////////////////////////////////////////
//
// Name           : Vector3D
// Inheritance    : Base struct 
// Desctription   : See header.
//
///////////////////////////////////////////////////////////////////////////////

geo::Vector3D::Vector3D() {};

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D::Vector3D(double X, double Y, double Z)
{
  myX = X;
  myY = Y;
  myZ = Z;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D&
geo::Vector3D::operator =(const Vector3D& RHS)
{
  myX = RHS.X();
  myY = RHS.Y();
  myZ = RHS.Z();
  return *this;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D
geo::Vector3D::operator +(const Vector3D& RHS) const
{
  return Vector3D( myX + RHS.X(), myY + RHS.Y(), myZ + RHS.Z() );
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D
geo::Vector3D::operator -(const Vector3D& RHS) const
{
  return Vector3D( myX - RHS.X(), myY - RHS.Y(), myZ - RHS.Z() );
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D 
geo::Vector3D::operator *(double RHS) const
{
  return Vector3D( myX*RHS, myY*RHS, myZ*RHS );
}
 
///////////////////////////////////////////////////////////////////////////////
geo::Vector3D 
geo::Vector3D::operator /(double RHS) const
{
  return Vector3D( myX/RHS, myY/RHS, myZ/RHS );
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D& 
geo::Vector3D::operator +=(const Vector3D& RHS)
{
  myX += RHS.X();
  myY += RHS.Y();
  myZ += RHS.Z();
  return *this; 
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D& 
geo::Vector3D::operator -=(const Vector3D& RHS)
{
  myX -= RHS.X();
  myY -= RHS.Y();
  myZ -= RHS.Z();
  return *this;   
}

///////////////////////////////////////////////////////////////////////////////
bool 
geo::Vector3D::operator ==(const Vector3D& RHS) const
{
  return ( myX == RHS.X() && myY == RHS.Y() && myZ == RHS.Z() );
}

///////////////////////////////////////////////////////////////////////////////
bool 
geo::Vector3D::operator !=(const Vector3D& RHS) const
{
  return !operator==(RHS);
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D 
geo::Vector3D::operator -() const
{
  return Vector3D(-myX, -myY, -myZ);
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D 
geo::Vector3D::operator *(const Vector3D& RHS) const
{
  return Vector3D( myY*RHS.Z() - myZ*RHS.Y(),
                   myZ*RHS.X() - myX*RHS.Z(),
                   myX*RHS.Y() - myY*RHS.X() );
}

///////////////////////////////////////////////////////////////////////////////
double 
geo::Vector3D::Length() const
{
  return num::Sqrt( LengthSquared() );
}
  
///////////////////////////////////////////////////////////////////////////////
double 
geo::Vector3D::LengthSquared() const
{
  return myX * myX + myY * myY + myZ * myZ;
}

///////////////////////////////////////////////////////////////////////////////
void 
geo::Vector3D::Normalise()
{
  double length = Length();
  
  if ( length == 0 )
  {
    *this = Vector3D::Null();
    return;
  }
  
  myX /= length;
  myY /= length;
  myZ /= length;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D& 
geo::Vector3D::Zero()
{
  static Vector3D vec(0, 0, 0);
  return vec;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D& 
geo::Vector3D::Null()
{
  static Vector3D vec;
  return vec;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D& 
geo::Vector3D::UnitX()
{
  static Vector3D vec(1, 0, 0);
  return vec;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D& 
geo::Vector3D::UnitY()
{
  static Vector3D vec(0, 1, 0);
  return vec;
}

///////////////////////////////////////////////////////////////////////////////
geo::Vector3D& 
geo::Vector3D::UnitZ()
{
  static Vector3D vec(0, 0, 1);
  return vec;
}
